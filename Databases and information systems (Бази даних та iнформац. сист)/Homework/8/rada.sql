drop table authors;
drop table votes;
drop table laws;

create table laws(
  id number primary key,  -- номер проекту закона
  title varchar(500),  -- назва проекту
  law_registation_id varchar(20),  -- регєстраціний номер закону (ми не використ.)
  initiator varchar(100) -- хто ініціював розгляд 
);

create table authors(
  law_id number references laws(id),  -- номер проекту закона
  author varchar(50),  -- і'мя автора
  primary key (law_id, author)
);

create table votes(
  law_id number references laws(id),  -- номер проекту закона
  pearson varchar(50),  -- і'мя автора
  vote varchar(30),  -- результат голосування
  primary key (law_id, pearson)
);


